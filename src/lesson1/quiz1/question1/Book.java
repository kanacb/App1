package lesson1.quiz1.question1;

public class Book {

    private String name;
    private String author;

    Book() {
    }

    Book(String name, String author) {
        this.name = name;
        this.author = author;
    }

    public String getName() {
        return name;
    }

    public String getAuthor() {
        return author;
    }

}
